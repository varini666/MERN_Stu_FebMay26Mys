//Comparison Operators
console.log("Comparison Operators");
let x = 2, y = '3';
console.log("x==y:",x==y);//Loose equality(values are cmp not DT)
console.log("x===y:",x===y);//strict equality (values & DT is cmp)
console.log("x!=y:",x!=y);//Loose inequality
console.log("x!==y:",x!==y);//strict equality
console.log("x>3:",x>3);
console.log("x<3:",x<3);
console.log("x>=3:",x>=3);
console.log("x<=3:",x<=3);